// GetIntegrationStatus checks the CloudFormation stack status for the Datadog integration.
func GetIntegrationStatus(
	ctx context.Context,
	clients *awsclient.Factory,
	cfg *config.Config,
) (*types.IntegrationStatusResponse, error) {
	cfClient, err := clients.CloudFormation(ctx)
	if err != nil {
		return nil, fmt.Errorf("failed to create CloudFormation client: %w", err)
	}

	resp, err := cfClient.DescribeStacks(ctx, &cloudformation.DescribeStacksInput{
		StackName: aws.String(DatadogStackName),
	})
	if err != nil {
		if isStackNotFound(err) {
			return &types.IntegrationStatusResponse{
				Status:      "not_setup",
				LastChecked: time.Now().UTC().Format(time.RFC3339),
			}, nil
		}
		return nil, fmt.Errorf("failed to describe stack %s: %w", DatadogStackName, err)
	}

	if len(resp.Stacks) > 0 {
		stack := resp.Stacks[0]
		status := string(stack.StackStatus)

		result := &types.IntegrationStatusResponse{
			StackName:   DatadogStackName,
			StackStatus: status,
			Method:      "metric_streams",
			LastChecked: time.Now().UTC().Format(time.RFC3339),
		}

		switch {
		case isActiveStatus(stack.StackStatus):
			result.Status = "active"
			roleArn := getRoleArnFromStack(&stack)
			if roleArn != "" {
				// We support dynamic Datadog API registration if an AppKey is supplied
				// We map the App Key dynamically from configuration (falls back to User API key slot)
				appKey := cfg.APIKey
				if appKey != "" {
					err := linkDatadogAccount(ctx, cfg, roleArn, appKey)
					if err != nil {
						result.Status = "error"
						result.Error = "AWS stack deployed, but failed to link with Datadog account: " + err.Error()
					}
				}
			}
		case isInProgressStatus(stack.StackStatus):
			result.Status = "in_progress"
		default:
			result.Status = "error"
			if stack.StackStatusReason != nil && *stack.StackStatusReason != "" {
				result.Error = *stack.StackStatusReason
			} else {
				result.Error = fmt.Sprintf("Stack is in failed state: %s. Please check the AWS CloudFormation Console for details.", status)
			}
		}

		return result, nil
	}

	return &types.IntegrationStatusResponse{
		Status:      "not_setup",
		LastChecked: time.Now().UTC().Format(time.RFC3339),
	}, nil
}

// SetupIntegration deploys Datadog's official main.yaml integration role stack.
func SetupIntegration(
	ctx context.Context,
	clients *awsclient.Factory,
	cfg *config.Config,
	req *types.IntegrationSetupRequest,
) (*types.IntegrationSetupResponse, error) {
	cfClient, err := clients.CloudFormation(ctx)
	if err != nil {
		return nil, fmt.Errorf("failed to create CloudFormation client: %w", err)
	}

	stsClient, err := clients.STS(ctx)
	if err != nil {
		return nil, fmt.Errorf("failed to create STS client: %w", err)
	}

	callerIdentity, err := stsClient.GetCallerIdentity(ctx, &sts.GetCallerIdentityInput{})
	if err != nil {
		return nil, fmt.Errorf("failed to get AWS account ID: %w", err)
	}
	awsAccountID := aws.ToString(callerIdentity.Account)

	// Build safe ExternalId seed
	externalID := "datadog-integration-" + awsAccountID

	params := []cftypes.Parameter{
		{ParameterKey: aws.String("ExternalId"), ParameterValue: aws.String(externalID)},
		{ParameterKey: aws.String("IAMRoleName"), ParameterValue: aws.String("DatadogIntegrationRole")},
	}

	// Check if stack already exists
	describeResp, err := cfClient.DescribeStacks(ctx, &cloudformation.DescribeStacksInput{
		StackName: aws.String(DatadogStackName),
	})
	if err == nil && len(describeResp.Stacks) > 0 {
		existingStack := describeResp.Stacks[0]
		status := existingStack.StackStatus

		if isFailedStatus(status) {
			_, err = cfClient.DeleteStack(ctx, &cloudformation.DeleteStackInput{
				StackName: aws.String(DatadogStackName),
			})
			if err != nil {
				return nil, fmt.Errorf("failed to delete failed existing stack %s: %w", DatadogStackName, err)
			}

			deleted := false
			for i := 0; i < 30; i++ {
				time.Sleep(2 * time.Second)
				_, err = cfClient.DescribeStacks(ctx, &cloudformation.DescribeStacksInput{
					StackName: aws.String(DatadogStackName),
				})
				if err != nil && isStackNotFound(err) {
					deleted = true
					break
				}
			}
			if !deleted {
				return nil, fmt.Errorf("timeout waiting for deletion of failed stack %s", DatadogStackName)
			}
		} else if isInProgressStatus(status) {
			return &types.IntegrationSetupResponse{
				Status:  "in_progress",
				StackID: aws.ToString(existingStack.StackId),
			}, nil
		} else if !isFailedStatus(status) && status != cftypes.StackStatusDeleteComplete {
			return &types.IntegrationSetupResponse{
				Status:  "active",
				StackID: aws.ToString(existingStack.StackId),
			}, nil
		}
	}

	createResp, err := cfClient.CreateStack(ctx, &cloudformation.CreateStackInput{
		StackName:    aws.String(DatadogStackName),
		TemplateBody: aws.String(DatadogTemplateBody),
		Parameters:   params,
		Capabilities: []cftypes.Capability{
			cftypes.CapabilityCapabilityIam,
			cftypes.CapabilityCapabilityNamedIam,
			cftypes.CapabilityCapabilityAutoExpand,
		},
	})
	if err != nil {
		return nil, fmt.Errorf("failed to create Datadog CF stack: %w", err)
	}

	return &types.IntegrationSetupResponse{
		Status:  "in_progress",
		StackID: aws.ToString(createResp.StackId),
	}, nil
}

// RemoveIntegration deletes the Datadog role stack and unlinks account registration.
func RemoveIntegration(
	ctx context.Context,
	clients *awsclient.Factory,
	cfg *config.Config,
) error {
	cfClient, err := clients.CloudFormation(ctx)
	if err != nil {
		return fmt.Errorf("failed to create CloudFormation client: %w", err)
	}

	stsClient, err := clients.STS(ctx)
	if err != nil {
		return fmt.Errorf("failed to create STS client: %w", err)
	}
	callerIdentity, err := stsClient.GetCallerIdentity(ctx, &sts.GetCallerIdentityInput{})
	if err != nil {
		return fmt.Errorf("failed to get AWS account ID: %w", err)
	}
	awsAccountID := aws.ToString(callerIdentity.Account)

	appKey := cfg.APIKey
	if appKey != "" {
		err := unlinkDatadogAccount(ctx, cfg, awsAccountID, appKey)
		if err != nil {
			fmt.Printf("warning: failed to unlink account from Datadog: %v\n", err)
		}
	}

	_, err = cfClient.DeleteStack(ctx, &cloudformation.DeleteStackInput{
		StackName: aws.String(DatadogStackName),
	})
	if err != nil && !isStackNotFound(err) {
		return fmt.Errorf("failed to delete Datadog stack: %w", err)
	}

	return nil
}

