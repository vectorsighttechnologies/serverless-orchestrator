func linkDatadogAccount(ctx context.Context, cfg *config.Config, roleArn, appKey string) error {
	parts := strings.Split(roleArn, ":")
	if len(parts) < 5 {
		return fmt.Errorf("invalid Role ARN: %s", roleArn)
	}
	awsAccountID := parts[4]

	endpoint := fmt.Sprintf("https://api.%s/api/v2/integration/aws/accounts", cfg.DDSite)
	if cfg.DDSite == "datadoghq.com" {
		endpoint = "https://api.datadoghq.com/api/v2/integration/aws/accounts"
	}

	payload := map[string]interface{}{
		"data": map[string]interface{}{
			"type": "aws_account",
			"attributes": map[string]interface{}{
				"account_id": awsAccountID,
				"role_name":  "DatadogIntegrationRole",
				"metrics_config": map[string]interface{}{
					"enabled":  true,
					"automute": true,
				},
			},
		},
	}

	bodyBytes, _ := json.Marshal(payload)
	req, err := http.NewRequestWithContext(ctx, "POST", endpoint, bytes.NewBuffer(bodyBytes))
	if err != nil {
		return err
	}

	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("DD-API-KEY", cfg.DDApiKey)
	req.Header.Set("DD-APPLICATION-KEY", appKey)

	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK && resp.StatusCode != http.StatusCreated {
		respBytes, _ := io.ReadAll(resp.Body)
		return fmt.Errorf("datadog API returned status %d: %s", resp.StatusCode, string(respBytes))
	}

	return nil
}

func unlinkDatadogAccount(ctx context.Context, cfg *config.Config, awsAccountID, appKey string) error {
	endpoint := fmt.Sprintf("https://api.%s/api/v2/integration/aws/accounts/%s", cfg.DDSite, awsAccountID)
	if cfg.DDSite == "datadoghq.com" {
		endpoint = "https://api.datadoghq.com/api/v2/integration/aws/accounts/" + awsAccountID
	}

	req, err := http.NewRequestWithContext(ctx, "DELETE", endpoint, nil)
	if err != nil {
		return err
	}

	req.Header.Set("DD-API-KEY", cfg.DDApiKey)
	req.Header.Set("DD-APPLICATION-KEY", appKey)

	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK && resp.StatusCode != http.StatusNoContent {
		respBytes, _ := io.ReadAll(resp.Body)
		return fmt.Errorf("datadog API returned status %d: %s", resp.StatusCode, string(respBytes))
	}

	return nil
}

